// Package mediahook предоставляет API для установки глобального low-level
// клавиатурного hook-а Windows, позволяющего перехватывать системные клавиши
// управления громкостью (Volume Up, Volume Down, Mute).
//
// Основной сценарий использования:
//  1. Создать хук через New(), передав callback-и на нужные клавиши.
//  2. При необходимости задать дополнительные опции, например WithMute().
//  3. Запустить хук методом Start() — он будет работать в отдельной горутине.
//  4. Завершить работу вызовом Close().
//
// Пример использования:
//
//	h, err := mediahook.New(
//		func() { fmt.Println("Volume Up") },
//		func() { fmt.Println("Volume Down") },
//		mediahook.WithMute(func() { fmt.Println("Muted") }),
//	)
//	if err != nil {
//		log.Fatal(err)
//	}
//	if err := h.Start(); err != nil {
//		log.Fatal(err)
//	}
//	defer h.Close()
//
// Пакет поддерживает только одну активную установку hook-а на процесс
// (ограничение WinAPI WH_KEYBOARD_LL). Повторная установка приведёт к ошибке.
//
// Все события клавиш по умолчанию «глушатся» (возвращается 1 из callback-а),
// поэтому системное поведение клавиши (например, изменение громкости)
// не выполняется. Если нужно пропускать событие дальше — логику нужно менять
// в функции llKeyboardProc.
